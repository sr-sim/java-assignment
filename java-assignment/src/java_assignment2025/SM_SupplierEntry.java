    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow >= 0 && selectedRow < jTable1.getRowCount()) {
            String supplierid = jTable1.getValueAt(selectedRow, 0).toString();
            String suppliername = jTable1.getValueAt(selectedRow, 1).toString();
            String contact = jTable1.getValueAt(selectedRow, 2).toString();
            String email = jTable1.getValueAt(selectedRow, 3).toString();
            String address = jTable1.getValueAt(selectedRow, 4).toString();
            String itemdesc = jTable1.getValueAt(selectedRow, 5).toString();
            
            jTextField1.setText(supplierid);
            jTextField2.setText(suppliername);
            jTextField3.setText(contact);
            jTextField4.setText(email);
            jTextField5.setText(address);
            jTextArea1.setText(itemdesc);
            try{
                jTable1.setRowSelectionInterval(selectedRow, selectedRow);
            }catch (IllegalArgumentException e){
                System.err.println("Error"+ e.getMessage());
            }
        }else{
            System.err.println("Selected row index is out of bounds: " + selectedRow);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MouseClicked


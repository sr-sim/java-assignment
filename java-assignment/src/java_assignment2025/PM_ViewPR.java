    private void importItemDetails(){
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
     
        prId.setText("Purchase Requisition ID: " + selectedPR.getPrid());
        requestDate.setText("Request Date:  " + selectedPR.getRequestdate());
        expectedDeliveryDate.setText("Expected Delivery Date: " +selectedPR.getExpecteddeliverydate());
        status.setText("Status: " + selectedPR.getApprovestatus());
        
        PurchaseRequisition.ApproveStatus approvalStatus = selectedPR.getApprovestatus(); 
        if (approvalStatus == PurchaseRequisition.ApproveStatus.approved || 
        approvalStatus == PurchaseRequisition.ApproveStatus.reject) {

        String byWho = selectedPR.getStatuschangeby();
        User user = userManager.findUserByID(byWho);
        String userName = (user != null) ? user.getFullname() : byWho;

        System.out.println("Test me");
        String labelPrefix = (approvalStatus == PurchaseRequisition.ApproveStatus.approved)
                             ? "Approved by: " : "Rejected by: ";
        jLabel5.setText(labelPrefix + byWho + " - " + userName);
    } else {
        jLabel5.setText(""); 
    }

        String prcreator = selectedPR.getUserid();
        User userofprcreator = userManager.findUserByID(prcreator);
        String prusername = (userofprcreator != null) ? userofprcreator.getFullname() : prcreator;
        createdBy.setText("PR Created by: " + prcreator+" - "+prusername);
        
        subtotal.setText("Subtotal: RM"+selectedPR.getTotal());
        note.setText("Note :  " + selectedPR.getNote());
        
        
        
        
        
        List<String> itemIds = selectedPR.getItemids();
        List<String> quantities = selectedPR.getQuantities();
        List<String> unitPrices = selectedPR.getUnitPrices();
        
        for (int i=0; i<itemIds.size();i++){
            String itemId = itemIds.get(i);
            String quantityRaw = (i < quantities.size()) ? quantities.get(i) : "0";
            String unitPriceRaw = (i < unitPrices.size()) ? unitPrices.get(i) : "0.00";

            Item item = inventorydatamanager.finditemid(itemId);
            String itemName = (item != null) ? item.getItemname() : "Unknown Item";

            // Calculate amount
            int quantity = 0;
            double unitPrice = 0.0;
            try {
                quantity = Integer.parseInt(quantityRaw);
                unitPrice = Double.parseDouble(unitPriceRaw);
            } catch (NumberFormatException e) {
                // Leave as 0 if parsing fails
            }
            double amount = quantity * unitPrice;
            
            model.addRow(new Object[] {
                itemId,
                itemName,
                quantity,
                unitPrice,
                amount
                
            });
            
        }
        
        
   }
